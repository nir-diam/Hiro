export const LanguageProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
    const [language, setLanguageState] = useState<Language>('he');
    const [dir, setDir] = useState<Direction>('rtl');

    useEffect(() => {
        // Persist language choice
        const savedLang = localStorage.getItem('app_language') as Language;
        if (savedLang && (savedLang === 'he' || savedLang === 'en')) {
            setLanguageState(savedLang);
        }
    }, []);

    useEffect(() => {
        // Update document direction and lang attribute dynamically
        const newDir = language === 'he' ? 'rtl' : 'ltr';
        setDir(newDir);
        document.documentElement.setAttribute('dir', newDir);
        document.documentElement.setAttribute('lang', language);
        localStorage.setItem('app_language', language);
    }, [language]);

    const setLanguage = (lang: Language) => {
        setLanguageState(lang);
    };

    // The Translation Function
    const t = (key: string, options?: { count?: number; [key: string]: any }) => {
        const keys = key.split('.');
        let current: any = resources[language];
        
        // @ts-ignore
        let value = current[key];

        if (value && options) {
            Object.keys(options).forEach(optKey => {
                 value = value.replace(`{{${optKey}}}`, options[optKey].toString());
            });
        }
        
        return value || key; // Fallback to key if not found
    };

    return (
        <LanguageContext.Provider value={{ language, setLanguage, dir, t }}>
            {children}
        </LanguageContext.Provider>
    );
};

export const useLanguage = () => {
    const context = useContext(LanguageContext);
    if (context === undefined) {
        throw new Error('useLanguage must be used within a LanguageProvider');
    }
    return context;
};
